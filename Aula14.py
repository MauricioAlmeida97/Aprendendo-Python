"""
Operadores de Atribuição
 = += -= /= //= *= **= %=
"""

contador = 0
contador += 1  # adiciona mais {valor}, neste caso 1
contador -= 1  # subtrai um {valor} da variável, neste caso 1
contador /= 1  # divide (float) do valor da variável
contador //= 1 # divide (int)  do valor da variável 
contador *= 1  # multiplica o valor da variável
contador **= 1 # eleva o contador à potencia indicada
 